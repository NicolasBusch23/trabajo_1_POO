package Ejercicio_14;

public class Operaciones {
    //Método o función para calcular el cuadrado de un número
    public static double calcular_cuadrado(double numero) {
        return Math.pow(numero, 2);
    }

    //Método o función para calcular el cubo de un número
    public static double calcular_cubo(double numero) {
        return Math.pow(numero, 3);
    }
}
